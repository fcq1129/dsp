#include "DSP28_Device.h"

/****************************************************************************
*
*名    称：InitGpio()
*
*功    能：初始化Gpio，使得Gpio的引脚处于已知的状态，例如确定其功能是特定功能
*          还是通用I/O。如果是通用I/O，是输入还是输出，等等。
*
*入口参数：无
*
*出口参数：无
*
****************************************************************************/

void InitGpio(void)
{

     EALLOW;
     
     // 在这里插入初始化函数的代码
	 GpioMuxRegs.GPDMUX.bit.T3CTRIP_PDPB_GPIOD5 = 0; ////液晶命令数据切换控制信号
  	 GpioMuxRegs.GPDDIR.bit.GPIOD5 = 1; //输出
  	 GpioMuxRegs.GPDMUX.bit.T4CTRIP_SOCB_GPIOD6 = 0;////液晶使能控制信号
  	 GpioMuxRegs.GPDDIR.bit.GPIOD6 = 1;//输出 
     
     EDIS;

}	
	
